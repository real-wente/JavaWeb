package Day1.junit;

/**
 * 计算器类
 * @author wentao
 * @date 2022-08-10  17:06
 */

public class Calculator {

    /**
     * 加法
     * @param num1
     * @param num2
     * @return
     */
    public int add (int num1,int num2){
        return num2+num1;
    }

    /**
     * 减法
     * @param num1
     * @param num2
     * @return
     */
    public int sub (int num1,int num2){
        return num1-num2;
    }




}
